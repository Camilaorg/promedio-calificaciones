    private void btnPromedioGeneralActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromedioGeneralActionPerformed
        // TODO add your handling code here:
        int mat = 0,es = 0,his = 0,pro = 0,eco = 0;
       double promedio;
       mat+=Double.parseDouble(txtMat.getText());
        mat+=Double.parseDouble(txtMat2.getText());
        mat+=Double.parseDouble(txtMat3.getText());
        
         es+=Double.parseDouble(txtEs.getText());
         es+=Double.parseDouble(txtEs2.getText());
         es+=Double.parseDouble(txtEs3.getText());
         
         his+=Double.parseDouble(txtHis.getText());
       his+=Double.parseDouble(txtHis2.getText());
       his+=Double.parseDouble(txtHis.getText());
       
      pro+=Double.parseDouble(txtPro.getText());
      pro+=Double.parseDouble(txtPro2.getText());
      pro+=Double.parseDouble(txtPro3.getText());
      
      eco+=Double.parseDouble(txtEco.getText());
      eco+=Double.parseDouble(txtEco2.getText());
      eco+=Double.parseDouble(txtEco3.getText());
      promedio=(mat+es+his+pro+eco)/15;
      lblMostrar.setText(""+promedio);
      
    }//GEN-LAST:event_btnPromedioGeneralActionPerformed

